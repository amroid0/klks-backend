        return $updatedCount;
    }

    
    private function getDriverCurrentLocation(User $driver): ?array
    {
        $realtimeLocationKey = "realtime_location_driver_{$driver->id}";
        $realtimeLocation = Cache::get($realtimeLocationKey);

        if ($realtimeLocation) {
            return $realtimeLocation;
        }

        $lastLocation = $driver->currentLocation;
        if ($lastLocation && $driver->last_latitude && $driver->last_longitude) {
            $timeSinceUpdate = now()->diffInSeconds($lastLocation->recorded_at);

            if ($timeSinceUpdate < 30) {
                return [
                    'latitude' => $driver->last_latitude,
                    'longitude' => $driver->last_longitude,
                    'heading' => $lastLocation->heading ?? 0,
                    'speed' => $lastLocation->speed ?? 0,
                    'accuracy' => $lastLocation->accuracy ?? 10,
                    'battery_level' => $lastLocation->battery_level ?? 80,
                    'is_charging' => $lastLocation->is_charging ?? false,
                ];
            }
        }

        if ($driver->last_latitude && $driver->last_longitude) {
            return [
                'latitude' => $driver->last_latitude,
                'longitude' => $driver->last_longitude,
                'heading' => 0,
                'speed' => 0,
                'accuracy' => 10,
                'battery_level' => 80,
                'is_charging' => false,
            ];
        }

        return null;
    }

    
