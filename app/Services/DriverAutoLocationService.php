use Illuminate\Support\Facades\Cache;

class DriverAutoLocationService
{
    
    public function startAutoLocationUpdates(User $driver): bool
    {
        try {
            // Check if driver using role_id instead of Spatie role
            if ($driver->role_id != 2 || !$driver->is_online) {
                
                return false;
            }

            $cacheKey = "auto_location_driver_{$driver->id}";
            Cache::put($cacheKey, [
                'driver_id' => $driver->id,
                'started_at' => now(),
                'last_update' => now(),
                'update_interval' => 10, // seconds
                'is_active' => true
            ], 3600); // Cache for 1 hour

            return true;
        } catch (\Exception $e) {
            return false;
        }
    }

    
    public function stopAutoLocationUpdates(User $driver): bool
    {
        try {
            $cacheKey = "auto_location_driver_{$driver->id}";
            Cache::forget($cacheKey);


            return true;
        } catch (\Exception $e) {
            return false;
        }
    }

    
    public function updateDriverLocation(User $driver, array $locationData): ?DriverLocation
    {
        try {
            // 🚫 Autogenerated driver check — HARD STOP
            // Autogenerated drivers have email pattern: {cityname}driver@etaxi.com
            if ($this->isAutogeneratedDriver($driver)) {
                
                return null;
            }

            if (!isset($locationData['latitude']) || !isset($locationData['longitude'])) {

                return null;
            }

            $driverLocation = DriverLocation::where('driver_id', $driver->id)
                ->where('is_active', true)
                ->first();

            if (!$driverLocation) {
                $driverLocation = new DriverLocation();
                $driverLocation->driver_id = $driver->id;
                $driverLocation->is_active = true;
            }

            $driverLocation->latitude = $locationData['latitude'];
            $driverLocation->longitude = $locationData['longitude'];
            $driverLocation->heading = $locationData['heading'] ?? 0;
            $driverLocation->speed = $locationData['speed'] ?? 0;
            $driverLocation->accuracy = $locationData['accuracy'] ?? 10;
            $driverLocation->battery_level = $locationData['battery_level'] ?? null;
            $driverLocation->is_charging = $locationData['is_charging'] ?? false;
            $driverLocation->address = $this->getAddressFromCoordinates(
                $locationData['latitude'],
                $locationData['longitude']
            );
            $driverLocation->recorded_at = now();

            $zone = $this->findZoneForLocation($locationData['latitude'], $locationData['longitude']);
            $driverLocation->zone_id = $zone?->id;

            $driverLocation->save();

            $driver->update([
                'last_latitude' => $locationData['latitude'],
                'last_longitude' => $locationData['longitude'],
                'last_location_at' => now(),
            ]);

            event(new DriverLocationAutoUpdate($driver, $driverLocation));

            $cacheKey = "auto_location_driver_{$driver->id}";
            $cacheData = Cache::get($cacheKey);
            if ($cacheData) {
                $cacheData['last_update'] = now();
                Cache::put($cacheKey, $cacheData, 3600);
            }

            

            return $driverLocation;
        } catch (\Exception $e) {
            return null;
        }
    }

    
    public function getActiveAutoLocationDrivers(): array
    {
        $drivers = [];
        $pattern = "auto_location_driver_*";

        $allOnlineDrivers = User::where('is_online', true)
            ->where('role_id', 2) // drivers only
            ->get();

        foreach ($allOnlineDrivers as $driver) {
            $cacheKey = "auto_location_driver_{$driver->id}";
            $cacheData = Cache::get($cacheKey);

            if ($cacheData && $cacheData['is_active']) {
                $drivers[] = [
                    'driver' => $driver,
                    'cache_data' => $cacheData
                ];
            }
        }

        return $drivers;
    }

    
    public function processAutoLocationUpdates(): int
    {
        $activeDrivers = $this->getActiveAutoLocationDrivers();
        $updatedCount = 0;

        foreach ($activeDrivers as $driverData) {
            $driver = $driverData['driver'];
            $cacheData = $driverData['cache_data'];

            $lastUpdate = $cacheData['last_update'] ?? now()->subMinutes(1);
            $shouldUpdate = now()->diffInSeconds($lastUpdate) >= $cacheData['update_interval'];

            if ($shouldUpdate) {
