                        'is_active' => false,
                        'stopped_at' => now()->toISOString()
                    ]
                ]);
            } else {
                return response()->json([
                    'success' => false,
                    'message' => 'Failed to stop auto location updates'
                ], 500);
            }
        } catch (\Exception $e) {
            return response()->json([
                'success' => false,
                'message' => 'Internal server error'
            ], 500);
        }
    }


    public function getAutoLocationStatus(Request $request): JsonResponse
    {
        try {
            $driver = Auth::user();

            if ($driver->role_id !== 2) {
                return response()->json([
                    'success' => false,
                    'message' => 'Unauthorized. Driver access required.'
                ], 401);
            }

            $cacheKey = "auto_location_driver_{$driver->id}";
            $cacheData = Cache::get($cacheKey);

            $status = [
                'driver_id' => $driver->id,
                'is_online' => $driver->is_online,
                'is_active' => $cacheData ? $cacheData['is_active'] : false,
                'started_at' => $cacheData ? $cacheData['started_at'] : null,
                'last_update' => $cacheData ? $cacheData['last_update'] : null,
                'update_interval' => $cacheData ? $cacheData['update_interval'] : 10,
                'websocket_enabled' => true,
                'timestamp' => now()->timestamp
            ];

            return response()->json([
                'success' => true,
                'data' => $status
            ]);
        } catch (\Exception $e) {
            return response()->json([
                'success' => false,
                'message' => 'Internal server error'
            ], 500);
        }
    }


    private function updateDriverLocationInDatabase(User $driver, array $locationData): ?DriverLocation
    {
        try {
            // 🚫 Autogenerated driver check — HARD STOP
            if ($this->isAutogeneratedDriver($driver)) {

                return null;
            }

            $driverLocation = DriverLocation::where('driver_id', $driver->id)
                ->where('is_active', true)
                ->first();

            if (!$driverLocation) {
                $driverLocation = new DriverLocation();
                $driverLocation->driver_id = $driver->id;
                $driverLocation->is_active = true;
            }

            $driverLocation->latitude = $locationData['latitude'];
            $driverLocation->longitude = $locationData['longitude'];
            $driverLocation->heading = $locationData['heading'];
            $driverLocation->speed = $locationData['speed'];
            $driverLocation->accuracy = $locationData['accuracy'];
            $driverLocation->battery_level = $locationData['battery_level'];
            $driverLocation->is_charging = $locationData['is_charging'];
            $driverLocation->address = $this->getAddressFromCoordinates(
                $locationData['latitude'],
                $locationData['longitude']
            );
            $driverLocation->recorded_at = $locationData['timestamp'];

            $zone = $this->findZoneForLocation($locationData['latitude'], $locationData['longitude']);
            $driverLocation->zone_id = $zone?->id;

            $driverLocation->save();

            return $driverLocation;
        } catch (\Exception $e) {
            return null;
        }
    }


