    public function updateLocation(Request $request)
    {
        try {
            $validator = Validator::make($request->all(), [
                'latitude' => 'required|numeric|between:-90,90',
                'longitude' => 'required|numeric|between:-180,180',
                'accuracy' => 'nullable|numeric|min:0',
                'speed' => 'nullable|numeric|min:0',
                'heading' => 'nullable|numeric|between:0,360',
                'timestamp' => 'nullable|date',
                'battery_level' => 'nullable|numeric|between:0,100',
                'is_charging' => 'nullable|boolean'
            ]);

            if ($validator->fails()) {
                return response()->json([
                    'success' => false,
                    'message' => 'Validation failed',
                    'errors' => $validator->errors()
                ], 422);
            }

            $driver = Auth::user();

            if ($driver->role_id != 2) {
                return response()->json([
                    'success' => false,
                    'message' => 'Unauthorized. Driver access required.'
                ], 401);
            }

            // 🚫 Autogenerated driver check — HARD STOP
            // Autogenerated drivers have email pattern: {cityname}driver@etaxi.com
            if ($this->isAutogeneratedDriver($driver)) {


                return response()->json([
                    'success' => true,
                    'message' => 'Autogenerated driver location update ignored'
                ], 200);
            }

            // 🚫 Demo driver check — HARD STOP
            $isDemo = false;
            $demoReason = '';

            if ($driver->email && str_ends_with($driver->email, '@etaxi.com')) {
                $isDemo = true;
                $demoReason = 'email_ends_with_etaxi';
            } elseif ($driver->phone && str_starts_with($driver->phone, '999999')) {
                $isDemo = true;
                $demoReason = 'phone_starts_with_999999';
            }
            if ($isDemo) {


                return response()->json([
                    'success' => true,
                    'message' => 'Demo driver location update ignored'
                ], 200);
            }

            $address = $this->getAddressFromCoordinates($request->latitude, $request->longitude);

            $zoneId = $this->getZoneIdFromCoordinates($request->latitude, $request->longitude);

            $locationData = [
                'driver_id' => $driver->id,
                'zone_id' => $zoneId,
                'latitude' => $request->latitude,
                'longitude' => $request->longitude,
                'address' => $address,
                'recorded_at' => $request->timestamp ? now()->parse($request->timestamp) : now(),
                'updated_at' => now()
            ];

            $driverLocation = DriverLocation::updateOrCreate(
                ['driver_id' => $driver->id],
                $locationData
            );

            // Update driver_attendance table
            $this->updateDriverAttendance($driver, $request, $address, $zoneId);

            try {
                event(new DriverLocationUpdated($driver, $driverLocation));
            } catch (\Exception $e) {
                // Continue on error
            }

            $toString = function ($value) {
                return is_null($value) ? '' : (string) $value;
            };

            return response()->json([
                'success' => true,
                'message' => 'Location updated successfully',
                'data' => [
                    'location_id' => $toString($driverLocation->id),
                    'zone_id' => $toString($driverLocation->zone_id),
                    'latitude' => $toString($driverLocation->latitude),
                    'longitude' => $toString($driverLocation->longitude),
                    'address' => $toString($driverLocation->address ?? $address),
                    'accuracy' => $toString($driverLocation->accuracy ?? ''),
                    'speed' => $toString($driverLocation->speed ?? ''),
                    'heading' => $toString($driverLocation->heading ?? ''),
                    'battery_level' => $toString($driverLocation->battery_level ?? ''),
                    'is_charging' => $toString($driverLocation->is_charging ?? ''),
                    'recorded_at' => $toString($driverLocation->recorded_at),
                    'updated_at' => $toString($driverLocation->updated_at)
                ]
            ]);
        } catch (\Exception $e) {

            return response()->json([
                'success' => false,
                'message' => 'Internal server error',
                'error' => config('app.debug') ? $e->getMessage() : 'Something went wrong'
            ], 500);
        }
    }

    public function getCurrentLocation()
    {
        try {
            $driver = Auth::user();

            if (!$driver || (!$driver->hasRole('driver') && $driver->role_id !== 2)) {
                return response()->json([
                    'success' => false,
                    'message' => 'Unauthorized. Driver access required.'
                ], 401);
            }

            $currentLocation = $driver->currentLocation;

            if (!$currentLocation) {
                return response()->json([
                    'success' => false,
                    'message' => 'No location data available'
                ], 404);
            }

            return response()->json([
                'success' => true,
