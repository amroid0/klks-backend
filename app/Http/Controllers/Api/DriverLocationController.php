    private function updateDriverAttendance($driver, $request, $address, $zoneId)
    {
        try {
            // Get or create active attendance record for today
            $attendance = DriverAttendance::where('driver_id', $driver->id)
                ->where('date', now()->toDateString())
                ->whereNull('offline_time')
                ->latest('online_time')
                ->first();

            // Prepare meta_data with location information
            $metaData = [
                'device_info' => $request->input('device_info'),
                'app_version' => $request->input('app_version'),
                'location' => [
                    'latitude' => $request->latitude,
                    'longitude' => $request->longitude,
                    'address' => $address,
                    'zone_id' => $zoneId,
                    'accuracy' => $request->input('accuracy'),
                    'speed' => $request->input('speed'),
                    'heading' => $request->input('heading'),
                    'battery_level' => $request->input('battery_level'),
                    'is_charging' => $request->input('is_charging'),
                    'timestamp' => now()->toIso8601String(),
                ],
            ];

            if ($attendance) {
                // Update existing attendance record with latest location in meta_data
                $existingMetaData = $attendance->meta_data ?? [];
                $existingMetaData['location'] = $metaData['location'];
                if (isset($metaData['device_info'])) {
                    $existingMetaData['device_info'] = $metaData['device_info'];
                }
                if (isset($metaData['app_version'])) {
                    $existingMetaData['app_version'] = $metaData['app_version'];
                }

                // Update meta_data and touch the updated_at timestamp
                $attendance->meta_data = $existingMetaData;
                $attendance->save();
            } else {
                // Create new attendance record if driver is online but no active session exists
                DriverAttendance::create([
                    'driver_id' => $driver->id,
                    'online_time' => now(),
                    'date' => now()->toDateString(),
                    'meta_data' => $metaData,
                ]);
            }
        } catch (\Exception $e) {
            // Log error silently
        }
    }

    /**
     * Check if driver is autogenerated
     * Autogenerated drivers have email pattern: {cityname}driver@etaxi.com
     *
     * @param User $driver
     * @return bool
     */
    private function isAutogeneratedDriver(User $driver): bool
    {
        if (!$driver->email) {
            return false;
        }

        // Check if email matches pattern: {cityname}driver@etaxi.com
        // Pattern: any characters followed by "driver@etaxi.com"
        $pattern = '/^.+driver@etaxi\.com$/i';

        if (preg_match($pattern, $driver->email)) {
            return true;
        }

        return false;
    }
